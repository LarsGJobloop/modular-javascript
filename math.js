/**
 * The math library version
 */
export const version = 1

/**
 * Sums two numbers
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns 
 */
export function sum(a, b) {
  return a + b
}

/**
 * Multiplies two numbers
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns 
 */
export function multiply(a, b) {
  return a * b
}
